package model;

public class Friend {
    private String libraryId;
    private String friendLibraryId;


    public String getLibraryId() {
        return libraryId;
    }

    public void setLibraryId(String libraryId) {
        this.libraryId = libraryId;
    }

    public String getFriendLibraryId() {
        return friendLibraryId;
    }

    public void setFriendLibraryId(String friendLibraryId) {
        this.friendLibraryId = friendLibraryId;
    }


}
